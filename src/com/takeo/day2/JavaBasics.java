package com.takeo.day2;

public class JavaBasics {

    public static void main(String[] args) {

        int a = 10;
        char c = 'd';
        double d = 1.0;
        float f = 2.0f;
        boolean b = false;


        System.out.println("this is an integer" + a);
        System.out.println("this is a char" + c);
        System.out.println("this is a double" + d);
        System.out.println("this is a float" + f);
        System.out.println("this is a float" + b);




    }
}
